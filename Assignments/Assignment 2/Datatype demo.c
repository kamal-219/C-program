#include <stdio.h>

int main() {
    // Declaration and initialization of different data types
    int age = 25;
    float height = 5.9f;
    char grade = 'A';
    double salary = 50000.75;

    // Display the values
    printf("Demonstration of basic data types:\n\n");

    printf("Integer (int)       : Age = %d\n", age);
    printf("Floating-point (float) : Height = %.2f\n", height);
    printf("Character (char)    : Grade = %c\n", grade);
    printf("Double (double)     : Salary = %.2lf\n", salary);

    return 0;
}
