//program to calculate the square of a number using a variable and assign the result to another variable.
#include <stdio.h>

int main() {
    int number = 7;       // Input number
    int square;           // Variable to store the square

    square = number * number;  // Calculate square

    printf("Number: %d\n", number);
    printf("Square: %d\n", square);

    return 0;
}
