  // Arithmetic operations of two numbers
#include <stdio.h>

int main() {
    int a,b;
    printf("enter the a value");
    scanf("%d",&a);
    printf("enter the b value");
    scanf("%d",&b);
    
    
    printf("addition %d\n",a+b);
    printf("sub %d\n",a-b);
    printf("multiply %d\n",a*b);
    printf("division r %d\n",a/b);
    printf("division q %d\n",a%b);
    
    return 0;
}
